    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtRole.getText().isEmpty() || txtPhone.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            txtRole.requestFocusInWindow();
        }
        else
        {
            seller = new Seller();
            seller.setRole(txtRole.getText());
            seller.setPhone(txtPhone.getText());
            seller.setCity(txtCity.getText());
            
            try {
                sellerDAO.Save(seller);
            } catch (SQLException e) {
                Logger.getLogger(SellerView.class.getName()).log(Level.SEVERE, null, e);
            }
            
            JOptionPane.showMessageDialog(null, "Gravado com sucesso!");
            
        }
    }//GEN-LAST:event_btnSaveActionPerformed

