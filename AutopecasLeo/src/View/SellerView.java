    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(txtId.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Selecione um vendedor");
        }
        else
        {
            seller = new Seller();
            seller.setId(Integer.parseInt(txtId.getText()));
            int confirm = JOptionPane.showConfirmDialog(null, "Deseja excluir: " + txtRole.getText());
            if (confirm == 0)
            {
                try {
                    sellerDAO.Delete(seller);
                } catch (SQLException e) {
                    Logger.getLogger(ClientView.class.getName()).log(Level.SEVERE, null, e);
                }
                JOptionPane.showMessageDialog(null, "Registro excluido com sucesso!");
                clearInputsSeller();
            }

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

