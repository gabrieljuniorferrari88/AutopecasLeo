    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        if(txtId.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Selecione um vendedor");
        }
        else
        {
            seller = new Seller();
            seller.setId(Integer.parseInt(txtId.getText()));
            seller.setRole(txtRole.getText());
            seller.setPhone(txtPhone.getText());
            seller.setCity(txtCity.getText());
            try {
                sellerDAO.Edit(seller);
            } catch (Exception e) {
                Logger.getLogger(ClientView.class.getName()).log(Level.SEVERE, null, e);
            }
            JOptionPane.showMessageDialog(null, "Registro alterado com sucesso!");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String id = JOptionPane.showInputDialog("Digite o código do Vendedor");
        seller = new Seller();
        try {
            seller = sellerDAO.SearchSellerId(id);
        } catch (Exception e) {
            Logger.getLogger(ClientView.class.getName()).log(Level.SEVERE, null, e);
        }
        
        txtId.setText(String.valueOf(seller.getId()));
        txtRole.setText(seller.getRole());
        txtCity.setText(seller.getCity());
        txtPhone.setText(seller.getPhone());
    }//GEN-LAST:event_btnSearchActionPerformed
    
